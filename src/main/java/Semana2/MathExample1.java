package Semana2;

public class MathExample1 {
    public static void main(String[] args) {
        // Número del cual se desea calcular la raíz cuadrada
        double number = 49.0;
        
        // Calcular la raíz cuadrada utilizando Math.sqrt
        double squareRoot = Math.sqrt(number);
        
        // Imprimir el resultado
        System.out.println("La raíz cuadrada de " + number + " es: " + squareRoot);
    }
}